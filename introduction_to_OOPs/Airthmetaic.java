class Airthmetaic {
    public static void main(String[] args) {
        int num1 = 12;
        int num2 = 4;
        int mul = num1 * num2;
        int div = num1 / num2;
        int mod = num1 % num2;
        System.out.print("Num1: " + num1);
        System.out.print("\n");
        System.out.print("Num2: " + num2);
        System.out.print("\n");
        System.out.print(num1 + " * " + num2 + " = " + mul);
        System.out.print("\n");
        System.out.print(num1 + " / " + num2 + " = " + div);
        System.out.print("\n");
        System.out.print(num1 + " % " + num2 + " = " + mod);
        System.out.print("\n");
    }    
}
